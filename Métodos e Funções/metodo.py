# Cria uma simples lista
l = [1,2,3,4,5]

'''
--- Métodos de uma lista ---

* append
* count
* extend
* insert
* pop
* remove
* reverse
* sort

'''

# Adiciona um elemento à lista usando o método 'append' e exibe-a em seguida
l.append(6)
# resultado '[1,2,3,4,5,6]'
print(l)

# Contando quantas vezes um elemento se repete na lista usando o método 'count' e em seguida mostra a quantidade de repetições
# resultado '1'
print(l.count(2))
