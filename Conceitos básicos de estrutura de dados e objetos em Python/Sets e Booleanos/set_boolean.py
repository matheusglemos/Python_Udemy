# Lista

lista = [1,2,1,2,34,521,4121,51515]

# Criando um set

numeros = set()

# Adicionando valores ao set

numeros.add(1)
numeros.add(2)
numeros.add(3)
numeros.add(2)
numeros.add(4)

# Exibindo os valores armazenados no set, não há valores repetidos, pois set é uma estrutura que não aceita repetições
print(numeros)
# Exibindo os valores armazenados no set
print(set(lista))

# Booleanos

# True para verdadeiro
# False para falso
# None como um espaço reservado para um objeto que não queremos reatribuir ainda

# resultado 'False'
print(1>2)
